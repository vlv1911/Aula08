# Criar um algoritimo que recebe um número e que o resultado seja o dobro:

def multiplicar(a):
    n = a * 2
    return n


numero = int(input('Digite o número: '))


resposta = multiplicar(numero)
print(f'O resultado é: {resposta}')



# Quadrado de um número:

def quadrado(q):
    n = q ** 2
    return n


numero = int(input('Digite o número: '))


resposta = quadrado(numero)
print(f'O quadrado é: {resposta}')



# Raiz quadrada:

def raiz(r):
    n = r ** 0.5
    return n


numero = int(input('Digite o número: '))


resposta = raiz(numero)
print(f'O quadrado é: {resposta}')